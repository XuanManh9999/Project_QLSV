public class day {
    private String maGV;
    private String maMH;

    public day(String maGV, String maMH) {
        this.maGV = maGV;
        this.maMH = maMH;
    }

    public String getMaGV() {
        return maGV;
    }

    public String getMaMH() {
        return maMH;
    }

    public void setMaGV(String maGV) {
        this.maGV = maGV;
    }

    public void setMaMH(String maMH) {
        this.maMH = maMH;
    }
    
}
